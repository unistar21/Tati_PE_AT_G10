package pretask;

public class IncrementDecrementExample {

    public static void main(String[] args) {
        for (int i = 3; i <= 10; i++) {
            System.out.println("инкремент:" + i);
        }
        for (int i = 10; i >= 1; i--) {
            System.out.println("декремент:" + i);
        }

    }
}