package pretask;

public class MathOperatorsExample {

    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        int sum = x + y;
        int subtr = x - y;
        int mult = x * y;
        int div = x / y;
        System.out.println("Сумма=" + sum);
        System.out.println("Вычитание=" + subtr);
        System.out.println("Умножение=" + mult);
        System.out.println("Деление=" + div);

    }
}
