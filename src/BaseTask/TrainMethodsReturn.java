package BaseTask;

public class TrainMethodsReturn {
    public int returnNewInt(int x) {
        return (x * 3);
    }

    ;

    public long returnNewLong(long y) {
        return (y - 4);
    }

    ;

    public char returnNewChar(char z) {
        return (char) (z + z);
    }

    ;

    public float returnNewFloat(float fl) {
        return (fl / 2);
    }

    ;

    public double returnNewDouble(double d) {
        return (d + 8);
    }

    ;

    public short returnNewShort(short sh) {
        return (short) (sh - 1);
    }

    ;

    public byte returnNewByte(byte bt) {
        return (byte) (bt * 2);
    }

    ;

    public boolean returnNewBoolean(boolean is) {
        return (!is);
    }

    ;
}
