package BaseTask;

public class TrainMethodsPrimitive {
    public void printInt(int x) {
        x = 21;
        System.out.println("я получил число " + x);
    }

    public void printLong(long y) {
        y = 2100120000;
        System.out.println("я получил длинное число " + y);
    }

    public void printChar(char z) {
        System.out.println("я получил символ " + z);
    }

    public void printFloat(float g) {
        System.out.println("я получил дробное число " + g);
    }

    public void printDouble(double s) {
        System.out.println("я получил длинное дробное число " + s);
    }

    public void printShort(short f) {
        System.out.println("я получил короткое число " + f);
    }

    public void printByte(byte n) {
        System.out.println("я получил очень короткое число " + n);
    }

    public void printBoolean(boolean n) {
        System.out.println("я получил булево " + n);
    }

}
